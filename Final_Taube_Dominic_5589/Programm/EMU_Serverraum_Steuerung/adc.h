//////////////////////////////////////////////////////////////////////////////////////
// Name         : adc.h
// Beschreibung : Header-Datei zur Initialisierung und Benutzung des ADC-Moduls
//                auf dem ATmega32. Enthält Funktionsprototypen für die
//                ADC-Initialisierung und das Auslesen von Einzel- sowie
//                Durchschnittswerten (float/int).
//////////////////////////////////////////////////////////////////////////////////////

#pragma once

#include <avr/io.h>

//////////////////////////////////////////////////////////////////////////////////////
// Name         : ADC_Init
// Beschreibung : Initialisiert den ADC mit AVCC als Referenz und Prescaler
// Parameter    : -
// Return       : -
//////////////////////////////////////////////////////////////////////////////////////
void ADC_Init(void);

//////////////////////////////////////////////////////////////////////////////////////
// Name         : ADC_Read
// Beschreibung : Liest den aktuellen Wert eines ADC-Kanals (0–7) aus
// Parameter    : uint8_t channel – ADC-Kanal (0–7)
// Return       : uint16_t – ADC-Wert (0–1023)
//////////////////////////////////////////////////////////////////////////////////////
uint16_t ADC_Read(uint8_t channel);

//////////////////////////////////////////////////////////////////////////////////////
// Name         : ADC_ReadAvgFloat
// Beschreibung : Gibt den Durchschnitt von 32 ADC-Messungen als Gleitkommazahl zurück
// Parameter    : uint8_t channel – ADC-Kanal (0–7)
// Return       : float – gemittelter ADC-Wert
//////////////////////////////////////////////////////////////////////////////////////
float ADC_ReadAvgFloat(uint8_t channel);

//////////////////////////////////////////////////////////////////////////////////////
// Name         : ADC_ReadAvgInt
// Beschreibung : Gibt den Durchschnitt von 32 ADC-Messungen als Ganzzahl zurück
// Parameter    : uint8_t channel – ADC-Kanal (0–7)
// Return       : uint8_t – gemittelter ADC-Wert (ganzzahlig)
//////////////////////////////////////////////////////////////////////////////////////
uint8_t ADC_ReadAvgInt(uint8_t channel);
